#include "libft.h"

/*
	DESCRIPTION
	The ft_lstsize() counts the number of nodes of the list.

	RETURN
	The ft_lstsize() returns the length of the list.
*/
int	ft_lstsize(t_list *lst)
{
	int	i;

	i = 0;
	while (lst)
	{
		lst = lst->next;
		i++;
	}
	return (i);
}
