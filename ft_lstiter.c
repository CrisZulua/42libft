#include "libft.h"

/*
	DESCRIPTION
	The ft_lstiter() function iterates through the list 'lst' and applies
	the function 'f' to the content of each node.
*/
void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	while (lst)
	{
		(*f)(lst->content);
		lst = lst->next;
	}
}
